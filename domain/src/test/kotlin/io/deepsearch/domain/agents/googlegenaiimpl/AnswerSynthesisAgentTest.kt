    @Test
    fun `should generate complete answer for well-covered query`() = runTest(testCoroutineDispatcher) {
        val source = ShortlistedSource(
            url = "https://example.com/neural-networks-guide",
            markdown = """
                # Neural Networks: A Complete Guide
                
                ## What are Neural Networks?
                Neural networks are computing systems inspired by the biological neural networks that constitute 
                animal brains. They are the foundation of deep learning and artificial intelligence.
                
                ## Structure
                A neural network consists of:
                1. **Neurons**: Basic computational units that receive inputs, process them, and produce outputs
                2. **Layers**: Collections of neurons organized into input, hidden, and output layers
                3. **Weights**: Parameters that determine the strength of connections between neurons
                4. **Activation Functions**: Mathematical functions that introduce non-linearity
                
                ## How They Work
                1. Input data is fed into the input layer
                2. Data flows through hidden layers where computations occur
                3. Each neuron applies weights to inputs and passes through activation function
                4. Final output is produced by the output layer
                5. Training adjusts weights through backpropagation to minimize error
                
                ## Types of Neural Networks
                - **Feedforward Neural Networks**: Information flows in one direction
                - **Convolutional Neural Networks (CNNs)**: Specialized for image processing
                - **Recurrent Neural Networks (RNNs)**: Handle sequential data
                - **Transformers**: State-of-the-art for natural language processing
                
                ## Applications
                - Image recognition and computer vision
                - Natural language processing and translation
                - Speech recognition and synthesis
                - Game playing and robotics
                - Drug discovery and medical diagnosis
            """.trimIndent(),
            sourceClassification = io.deepsearch.domain.models.valueobjects.SourceType.OFFICIAL_LIVING_DOC,
            contentDate = null,
            answerType = io.deepsearch.domain.models.valueobjects.AnswerType.DIRECT_ANSWER,
            relevanceJustification = "Comprehensive guide covering all aspects of neural networks"
        )

        val input = AnswerSynthesisInput(
            query = "Explain neural networks and how they work",
            shortlistedSources = listOf(source)
        )

        val output = agent.generate(input)

        assertNotNull(output)
