class AnswerSynthesisAgentTest : KoinTest {

    @JvmField
    @RegisterExtension
    val koin = KoinTestExtension.create { modules(domainTestModule) }

    private val testCoroutineDispatcher by inject<CoroutineDispatcher>()
    private val agent by inject<IAnswerSynthesisAgent>()

    @Test
    fun `should return default message when shortlisted sources are empty`() = runTest(testCoroutineDispatcher) {
        val input = AnswerSynthesisInput(
            query = "What is machine learning?",
            shortlistedSources = emptyList()
        )

        val output = agent.generate(input)

        assertNotNull(output)
        assertEquals("No information found to answer the query.", output.answer)
        assertNotNull(output.tokenUsage)
    }

    @Test
    fun `should generate comprehensive answer from single high-quality source`() = runTest(testCoroutineDispatcher) {
        val source = ShortlistedSource(
            url = "https://example.com/machine-learning",
            markdown = """
                # Machine Learning
                
                Machine learning is a subset of artificial intelligence (AI) that enables systems to learn 
                and improve from experience without being explicitly programmed. It focuses on developing 
                computer programs that can access data and use it to learn for themselves.
                
                ## How It Works
                
                The process of learning begins with observations or data, such as examples, direct experience, 
                or instruction, in order to look for patterns in data and make better decisions in the future 
                based on the examples that we provide.
                
                ## Key Concepts
                
                - **Training Data**: Data used to train the model
                - **Features**: Input variables used for predictions
                - **Model**: Mathematical representation learned from data
                - **Prediction**: Output generated by the model
            """.trimIndent(),
            sourceClassification = io.deepsearch.domain.models.valueobjects.SourceType.OFFICIAL_LIVING_DOC,
            contentDate = null,
            answerType = io.deepsearch.domain.models.valueobjects.AnswerType.DIRECT_ANSWER,
            relevanceJustification = "Comprehensive introduction to machine learning"
        )

        val input = AnswerSynthesisInput(
            query = "What is machine learning?",
            shortlistedSources = listOf(source)
        )

        val output = agent.generate(input)

        assertNotNull(output)
        assertTrue(output.answer.isNotBlank(), "Answer should not be blank")
        assertTrue(output.answer.length > 100, "Answer should be comprehensive")
        assertTrue(output.tokenUsage.totalTokens > 0, "Should track token usage")
    }

